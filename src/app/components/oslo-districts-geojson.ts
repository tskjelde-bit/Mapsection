import type { FeatureCollection, Feature, Polygon } from "geojson";

interface DistrictProperties {
  id: string;
  name: string;
}

function district(id: string, name: string, coordinates: number[][]): Feature<Polygon, DistrictProperties> {
  return {
    type: "Feature",
    properties: { id, name },
    geometry: {
      type: "Polygon",
      coordinates: [coordinates],
    },
  };
}

